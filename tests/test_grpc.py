"""Unit tests for the gRPC servicer."""

from __future__ import annotations

import importlib
import sys
from types import ModuleType
from unittest.mock import MagicMock, patch


def _build_fake_pb2_modules() -> tuple[ModuleType, ModuleType]:
    """Create lightweight protobuf stand-ins used by main_grpc in tests."""
    pb2 = ModuleType("nlp_service_pb2")

    class AnalyzeRequest:
        def __init__(
            self,
            text: str = "",
            categories: list[str] | None = None,
            tonalities: list[str] | None = None,
        ) -> None:
            self.text = text
            self.categories = categories or []
            self.tonalities = tonalities or []

    class CategoryScore:
        def __init__(self, category: str, score: float) -> None:
            self.category = category
            self.score = score

    class TonalityScore:
        def __init__(self, tonality: str, score: float) -> None:
            self.tonality = tonality
            self.score = score

    class AnalyzeResponse:
        def __init__(
            self,
            language: str,
            sentiment: str,
            sentiment_score: float,
            matched_categories: list[CategoryScore],
            matched_tonalities: list[TonalityScore],
            is_toxic: bool,
            highest_toxicity_score: float,
            processing_time_ms: float,
        ) -> None:
            self.language = language
            self.sentiment = sentiment
            self.sentiment_score = sentiment_score
            self.matched_categories = matched_categories
            self.matched_tonalities = matched_tonalities
            self.is_toxic = is_toxic
            self.highest_toxicity_score = highest_toxicity_score
            self.processing_time_ms = processing_time_ms

    class AnalyzeBatchRequest:
        def __init__(
            self,
            texts: list[str] | None = None,
            categories: list[str] | None = None,
            tonalities: list[str] | None = None,
        ) -> None:
            self.texts = texts or []
            self.categories = categories or []
            self.tonalities = tonalities or []

    class AnalyzeBatchResponse:
        def __init__(self, results: list[AnalyzeResponse]) -> None:
            self.results = results

    pb2.AnalyzeRequest = AnalyzeRequest
    pb2.CategoryScore = CategoryScore
    pb2.TonalityScore = TonalityScore
    pb2.AnalyzeResponse = AnalyzeResponse
    pb2.AnalyzeBatchRequest = AnalyzeBatchRequest
    pb2.AnalyzeBatchResponse = AnalyzeBatchResponse

    pb2_grpc = ModuleType("nlp_service_pb2_grpc")

    class NlpServiceServicer:
        """Base class stand-in generated by grpcio-tools."""

    pb2_grpc.NlpServiceServicer = NlpServiceServicer
    pb2_grpc.add_NlpServiceServicer_to_server = lambda *_args, **_kwargs: None

    return pb2, pb2_grpc


_PB2, _PB2_GRPC = _build_fake_pb2_modules()
sys.modules.setdefault("nlp_service_pb2", _PB2)
sys.modules.setdefault("nlp_service_pb2_grpc", _PB2_GRPC)
sys.modules.pop("main_grpc", None)
MAIN_GRPC = importlib.import_module("main_grpc")


def test_grpc_analyze_maps_engine_output() -> None:
    """Analyze RPC maps engine output to protobuf response fields."""
    engine = MagicMock()
    engine.analyze.return_value = {
        "language": "en",
        "sentiment": "positive",
        "sentiment_score": 0.91,
        "matched_categories": [
            {"category": "technology", "score": 0.83},
            {"category": "science", "score": 0.74},
        ],
        "matched_tonalities": [
            {"tonality": "formal", "score": 0.88},
            {"tonality": "objective", "score": 0.76},
        ],
        "is_toxic": False,
        "highest_toxicity_score": 0.07,
        "processing_time_ms": 10.0,
    }

    servicer = MAIN_GRPC.NlpServiceServicer(engine)
    request = _PB2.AnalyzeRequest(
        text="AI is changing the world.",
        categories=["technology", "science"],
        tonalities=["formal", "objective"],
    )

    response = servicer.Analyze(request, MagicMock())

    engine.analyze.assert_called_once_with(
        "AI is changing the world.",
        ["technology", "science"],
        ["formal", "objective"],
    )
    assert response.language == "en"
    assert response.sentiment == "positive"
    assert response.sentiment_score == 0.91
    assert len(response.matched_categories) == 2
    assert response.matched_categories[0].category == "technology"
    assert len(response.matched_tonalities) == 2
    assert response.matched_tonalities[0].tonality == "formal"
    assert response.is_toxic is False
    assert response.highest_toxicity_score == 0.07
    assert response.processing_time_ms >= 0


