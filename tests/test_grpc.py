def test_grpc_analyze_batch_maps_engine_output() -> None:
    """AnalyzeBatch RPC maps batch results correctly."""
    engine = MagicMock()
    engine.analyze_batch.return_value = [
        {
            "language": "en",
            "sentiment": "positive",
            "sentiment_score": 0.88,
            "matched_categories": [{"category": "tech", "score": 0.9}],
            "matched_tonalities": [{"tonality": "formal", "score": 0.72}],
            "is_toxic": False,
            "highest_toxicity_score": 0.09,
            "processing_time_ms": 30.0,
        },
        {
            "language": "fr",
            "sentiment": "negative",
            "sentiment_score": 0.81,
            "matched_categories": [{"category": "politics", "score": 0.64}],
            "matched_tonalities": [{"tonality": "aggressive", "score": 0.77}],
            "is_toxic": True,
            "highest_toxicity_score": 0.92,
            "processing_time_ms": 33.0,
        },
    ]

    servicer = MAIN_GRPC.NlpServiceServicer(engine)
    request = _PB2.AnalyzeBatchRequest(
        texts=["AI is useful.", "Je te deteste."],
        categories=["tech", "politics"],
        tonalities=["formal", "aggressive"],
    )

    response = servicer.AnalyzeBatch(request, MagicMock())

    engine.analyze_batch.assert_called_once_with(
        ["AI is useful.", "Je te deteste."],
        ["tech", "politics"],
        ["formal", "aggressive"],
    )
    assert len(response.results) == 2
    assert response.results[0].language == "en"
    assert response.results[1].is_toxic is True


def test_grpc_serve_starts_server() -> None:
    """serve() should initialize telemetry/instrumentation and start the gRPC server."""
    server = MagicMock()

    with (
        patch.object(MAIN_GRPC, "setup_logging") as setup_logging_mock,
        patch.object(MAIN_GRPC, "setup_telemetry") as setup_telemetry_mock,
        patch.object(MAIN_GRPC.nltk, "download") as nltk_download_mock,
        patch.object(MAIN_GRPC, "NlpEngine", return_value=MagicMock()) as engine_cls,
        patch.object(MAIN_GRPC, "GrpcInstrumentorServer") as instrumentor_cls,
        patch.object(MAIN_GRPC.grpc, "server", return_value=server),
        patch.object(MAIN_GRPC.nlp_service_pb2_grpc, "add_NlpServiceServicer_to_server") as add_srv,
    ):
        instrumentor_cls.return_value.instrument.return_value = None
        server.add_insecure_port.return_value = 50051
        server.wait_for_termination.return_value = None

        MAIN_GRPC.serve()

    setup_logging_mock.assert_called_once()
    setup_telemetry_mock.assert_called_once()
    nltk_download_mock.assert_called_once_with("punkt_tab", quiet=True)
    engine_cls.assert_called_once()
    instrumentor_cls.return_value.instrument.assert_called_once()
    add_srv.assert_called_once()
    server.start.assert_called_once()
    server.wait_for_termination.assert_called_once()
